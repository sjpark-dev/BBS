package com.mycompany.project.model;

public class Rating {
	private int bno;
	private String mid;
	private double midrating;
	
	public double getMidrating() {
		return midrating;
	}
	public void setMidrating(double midrating) {
		this.midrating = midrating;
	}
	public int getBno() {
		return bno;
	}
	public void setBno(int bno) {
		this.bno = bno;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	
	
}
